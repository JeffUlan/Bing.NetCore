using Bing.Samples.GraphQL.Types;
using GraphQL.Types;

namespace Bing.Samples.GraphQL
{
    public class HumanInputType : InputObjectGraphType<Human>
    {
        public HumanInputType()
        {
            Name = "HumanInput";
            Field(x => x.Name);
            Field(x => x.HomePlanet, nullable: true);
        }
    }
}
